package scr;

public class Array {

    public static void main(String[] args) {
        String [] cars = {"BMW","VOLVO","FORD"};
        
        for(String i : cars){
            System.out.println(i);
        }
        for(int i = 0; i < cars.length;  i++){
            System.out.println(cars[i]);
        }
        }
    
}
